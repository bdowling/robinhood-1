package com.kalki.robinhood.models;

public class MarketData {
    private String timezone;

    private String mic;

    private String acronym;

    private String website;

    private String name;

    private String operating_mic;

    private String url;

    private String todays_hours;

    private String country;

    private String city;

    public String getTimezone() {
        return timezone;
    }

    public void setTimezone(String timezone) {
        this.timezone = timezone;
    }

    public String getMic() {
        return mic;
    }

    public void setMic(String mic) {
        this.mic = mic;
    }

    public String getAcronym() {
        return acronym;
    }

    public void setAcronym(String acronym) {
        this.acronym = acronym;
    }

    public String getWebsite() {
        return website;
    }

    public void setWebsite(String website) {
        this.website = website;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getOperating_mic() {
        return operating_mic;
    }

    public void setOperating_mic(String operating_mic) {
        this.operating_mic = operating_mic;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getTodays_hours() {
        return todays_hours;
    }

    public void setTodays_hours(String todays_hours) {
        this.todays_hours = todays_hours;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }
}
