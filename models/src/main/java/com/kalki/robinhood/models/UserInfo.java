package com.kalki.robinhood.models;

public class UserInfo {
    private String id;

    private String first_name;

    private String username;

    private String employment;

    private String investment_profile;

    private String email;

    private String additional_info;

    private String last_name;

    private String international_info;

    private String id_info;

    private String basic_info;

    private String url;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getFirst_name() {
        return first_name;
    }

    public void setFirst_name(String first_name) {
        this.first_name = first_name;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getEmployment() {
        return employment;
    }

    public void setEmployment(String employment) {
        this.employment = employment;
    }

    public String getInvestment_profile() {
        return investment_profile;
    }

    public void setInvestment_profile(String investment_profile) {
        this.investment_profile = investment_profile;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getAdditional_info() {
        return additional_info;
    }

    public void setAdditional_info(String additional_info) {
        this.additional_info = additional_info;
    }

    public String getLast_name() {
        return last_name;
    }

    public void setLast_name(String last_name) {
        this.last_name = last_name;
    }

    public String getInternational_info() {
        return international_info;
    }

    public void setInternational_info(String international_info) {
        this.international_info = international_info;
    }

    public String getId_info() {
        return id_info;
    }

    public void setId_info(String id_info) {
        this.id_info = id_info;
    }

    public String getBasic_info() {
        return basic_info;
    }

    public void setBasic_info(String basic_info) {
        this.basic_info = basic_info;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
